#!/usr/bin/env node
/**
 * Generate (or refresh) the Ed25519 keypair used to sign update manifests.
 *
 * Plan A supply-chain protection (see docs/auto-update-design-2026-07-08.md §8):
 *   - PRIVATE key → stored OUTSIDE the repo (default ~/.synthetix/update-signing.key),
 *     file mode 0600. NEVER committed. Used only by scripts/publish-release.mjs.
 *   - PUBLIC key → src/generated/update-pubkey.ts (git-tracked), embedded in the
 *     app at build time, used by electron/updater.ts to verify manifests.
 *
 * Idempotent: if the private key already exists, it is PRESERVED and only the
 * public-key TS file is rewritten (so you can safely re-run to refresh the
 * generated file after a checkout). Pass --force to regenerate a brand-new
 * keypair (WARNING: invalidates all previously-signed manifests — existing
 * installs will refuse updates signed by the old key until they reinstall).
 *
 * Override the private-key location with SYNTHETIX_SIGNING_KEY_PATH (env) or
 * --key-path <file> (arg). This lets CI keep the key in a secret-stored path.
 */
import crypto from "node:crypto";
import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), "..");
// The pubkey must live UNDER electron/ so the Electron main-process tsconfig
// (rootDir "electron/", include "./**/*.ts") compiles it. The renderer never
// needs the pubkey directly — only the updater in the main process verifies.
const GEN_DIR = path.join(ROOT, "electron", "generated");

const args = new Set(process.argv.slice(2));
const FORCE = args.has("--force");
const keyPathIdx = process.argv.indexOf("--key-path");
const KEY_PATH_OVERRIDE =
  keyPathIdx > -1 ? process.argv[keyPathIdx + 1] : process.env.SYNTHETIX_SIGNING_KEY_PATH;

function defaultKeyPath() {
  return path.join(os.homedir(), ".synthetix", "update-signing.key");
}
function log(...m) {
  console.log(`\x1b[36m[signing-key]\x1b[0m`, ...m);
}
function warn(...m) {
  console.error(`\x1b[33m[signing-key:WARN]\x1b[0m`, ...m);
}
function fail(...m) {
  console.error(`\x1b[31m[signing-key:ERROR]\x1b[0m`, ...m);
  process.exit(1);
}

const keyPath = KEY_PATH_OVERRIDE || defaultKeyPath();
const pubkeyTsPath = path.join(GEN_DIR, "update-pubkey.ts");

function main() {
  fs.mkdirSync(GEN_DIR, { recursive: true });
  fs.mkdirSync(path.dirname(keyPath), { recursive: true });

  let privateKeyPem;
  let publicKeyDerHex;
  const exists = fs.existsSync(keyPath);

  if (exists && !FORCE) {
    // Preserve the existing private key; just re-derive + re-emit the pubkey file.
    privateKeyPem = fs.readFileSync(keyPath, "utf8");
    const priv = crypto.createPrivateKey(privateKeyPem);
    const pub = crypto.createPublicKey(priv);
    publicKeyDerHex = pub.export({ type: "spki", format: "der" }).toString("hex");
    log(`reusing existing private key at ${keyPath}`);
  } else {
    if (exists && FORCE) {
      warn(`--force: overwriting existing key at ${keyPath}`);
      warn(`PREVIOUSLY-SIGNED MANIFESTS WILL NO LONGER VERIFY on existing installs.`);
    }
    // Generate a fresh Ed25519 keypair.
    const { privateKey, publicKey } = crypto.generateKeyPairSync("ed25519");
    privateKeyPem = privateKey.export({ type: "pkcs8", format: "pem" });
    publicKeyDerHex = publicKey.export({ type: "spki", format: "der" }).toString("hex");

    // Write the private key with restrictive permissions. On Windows chmod is
    // largely advisory, but on macOS/Linux 0600 prevents other users reading it.
    fs.writeFileSync(keyPath, privateKeyPem, { encoding: "utf8", mode: 0o600 });
    log(`wrote private key → ${keyPath} (mode 0600)`);
  }

  // Emit the public-key TS file (git-tracked). Hex of the DER-encoded SPKI.
  const pubkeyTs = `// AUTO-GENERATED by scripts/generate-signing-key.mjs — do not edit by hand.
// Ed25519 PUBLIC key for update-manifest verification (Plan A supply-chain
// protection). Safe to commit; the matching private key is kept offline and
// never enters the repo. See electron/manifest-signing.ts.
export const updatePubkey = ${JSON.stringify(publicKeyDerHex)} as const;
`;
  fs.writeFileSync(pubkeyTsPath, pubkeyTs, "utf8");
  log(`wrote electron/generated/update-pubkey.ts (${publicKeyDerHex.slice(0, 16)}…)`);
  log(`\nNext:`);
  log(`  • The private key lives at ${keyPath} — back it up securely, it is NOT in git.`);
  log(`  • scripts/publish-release.mjs will find it automatically.`);
  log(`  • Re-run this script (no flags) to re-emit the pubkey file after a fresh checkout.`);
}

main();
