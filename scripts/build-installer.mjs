#!/usr/bin/env node
/**
 * Synthetix Windows installer — one-shot build script.
 *
 * Produces a distributable setup.exe from the current working tree by:
 *   1. Running `next build` to regenerate .next
 *   2. Assembling a self-contained bundle at dist/app (Node + .next + deps +
 *      Python runtime + workers + prisma + launchers)
 *   3. Aggressively trimming the Python runtime (removes dev-environment
 *      leakage + non-runtime CPython dirs) and cross-platform Node binaries
 *   4. Compiling the bundle with Inno Setup (iscc) into dist/installer
 *
 * Design notes:
 *   - Version comes from package.json (single source of truth — no .iss drift).
 *   - Uses `next start`, so the full production node_modules is shipped (not
 *     standalone). `.env` is auto-loaded by `next start`.
 *   - The bundled runtime/node.exe + runtime/python mean the target machine
 *     needs NOTHING else installed.
 *   - `.env` and `data/` are runtime-generated by first-run.js, so they are
 *     never part of the payload.
 *
 * Usage:  node scripts/build-installer.mjs [--no-build] [--no-trim]
 */
import { spawnSync } from "node:child_process";
import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { PYTHON_EXCLUDED_PACKAGES, normalizePkgName } from "./python-excluded-packages.mjs";
import {
  rmrf as rmrfLib,
  dirSize,
  countFiles,
  human,
  resolvePnpmPkgPath,
  resolveSymlink,
  copyDir,
  copyDirExcluding,
  copyFile,
  copyRootConfigs,
  copyStandalone,
  flattenPnpmStore,
  copyStaticAssets,
  generateLegalArtifacts,
} from "./lib/bundle-assembly.mjs";
import {
  rebuildNativeModulesForBundledNode,
  smokeNativeModule,
} from "./lib/native-rebuild.mjs";
import {
  createWindowsRuntimeFingerprint,
  validateOrInvalidateRuntimeCache,
} from "./lib/runtime-integrity.mjs";

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), "..");
const DIST = path.join(ROOT, "dist");
const APP = path.join(DIST, "app");
const INSTALLER_DIR = path.join(DIST, "installer");
const PACKAGING = path.join(ROOT, "packaging");
const runtimeVersions = JSON.parse(
  fs.readFileSync(path.join(ROOT, "config/runtime-versions.json"), "utf8"),
);
const REQUIREMENTS_PATH = path.join(ROOT, "workers", "python", "requirements.txt");
const RUNTIME_FINGERPRINT = createWindowsRuntimeFingerprint({
  runtimeVersions,
  requirementsPath: REQUIREMENTS_PATH,
});
const RUNTIME_MARKER = ".synthetix-runtime-fingerprint.json";

const args = new Set(process.argv.slice(2));
const SKIP_BUILD = args.has("--no-build");
const SKIP_TRIM = args.has("--no-trim");
// --assemble-only: stop after assembling dist/app WITHOUT running the legacy
// iscc step. Used by the Electron installer pipeline (build-electron.mjs path),
// which assembles dist/app here then wraps it in an Electron shell + its own
// Inno Setup (Synthetix-Electron.iss) to produce the final installer. If we also
// ran the legacy iscc here, both .iss files emit the same OutputBaseFilename
// (Synthetix-Setup-v<ver>.exe), and a leftover/aborted legacy .exe can shadow or
// collide with the Electron one — see packaging lessons "pitfall #5".
const ASSEMBLE_ONLY = args.has("--assemble-only");

// ---------- helpers ----------
// NOTE: the OS-agnostic helpers (rmrf, dirSize, countFiles, human,
// resolvePnpmPkgPath, resolveSymlink, copyDir, copyDirExcluding, copyFile, and
// the assembly functions copyRootConfigs/copyStandalone/flattenPnpmStore/
// copyStaticAssets/generateLegalArtifacts) now live in ./lib/bundle-assembly.mjs
// and are imported above. They are shared with build-installer-mac.mjs.
// `rmrf` takes (target, warn, rootDir) in the lib; this thin adapter keeps the
// call sites in this file unchanged (rmrf(target)) while delegating to the lib
// with this module's warn + ROOT — behavior-identical to the previous inline
// version. Verified by the byte-identical baseline check (plan Task 2).
function log(...m) {
  console.log(`\n\x1b[36m[build-installer]\x1b[0m`, ...m);
}
function warn(...m) {
  console.error(`\x1b[33m[build-installer:WARN]\x1b[0m`, ...m);
}
function fail(...m) {
  console.error(`\x1b[31m[build-installer:ERROR]\x1b[0m`, ...m);
  process.exit(1);
}
/** rmrf adapter: the lib's rmrf takes (target, warn, rootDir); supply our own. */
function rmrf(target) {
  return rmrfLib(target, warn, ROOT);
}

function run(cmd, cmdline, opts = {}) {
  log(`$ ${cmd} ${cmdline.join(" ")}`);
  const res = spawnSync(cmd, cmdline, { stdio: "inherit", shell: false, ...opts });
  if (res.status !== 0) fail(`command failed (exit ${res.status}): ${cmd} ${cmdline.join(" ")}`);
  return res;
}

// ---------- locate iscc ----------
function findIscc() {
  // 1. PATH
  const which = spawnSync("where", ["iscc"], { shell: true, encoding: "utf8" });
  if (which.status === 0) {
    const p = which.stdout.split(/\r?\n/)[0].trim();
    if (p && fs.existsSync(p)) return p;
  }
  // 2. winget default user install
  const local = path.join(os.homedir(), "AppData", "Local", "Programs", "Inno Setup 6", "iscc.exe");
  if (fs.existsSync(local)) return local;
  // 3. machine-wide defaults
  for (const base of [
    "C:\\Program Files (x86)\\Inno Setup 6\\iscc.exe",
    "C:\\Program Files\\Inno Setup 6\\iscc.exe",
  ]) {
    if (fs.existsSync(base)) return base;
  }
  fail(
    "Inno Setup (iscc.exe) not found.\n" +
      "Install it with:  winget install JRSoftware.InnoSetup\n" +
      "Then re-run this script."
  );
}

// ---------- main ----------
async function main() {
  const startedAt = Date.now();
  const pkg = JSON.parse(fs.readFileSync(path.join(ROOT, "package.json"), "utf8"));
  const VERSION = pkg.version;
  log(`Synthetix ${VERSION} — building Windows installer`);

  // Skip the Inno Setup lookup when only assembling dist/app (--assemble-only).
  // This path is used by build-electron.mjs (which wraps dist/app in its own
  // Electron installer) and by the byte-identical refactor check that runs on
  // macOS (where iscc is unavailable). Previously findIscc() ran unconditionally
  // and hard-failed on non-Windows, blocking --assemble-only entirely.
  const iscc = ASSEMBLE_ONLY ? null : findIscc();
  log(`iscc: ${ASSEMBLE_ONLY ? "(skipped, --assemble-only)" : iscc}`);

  // --- Step 1: build .next ---
  if (!SKIP_BUILD) {
    log("Step 1/5: next build");
    // The build regenerates the prisma client too (postinstall/prisma generate
    // is wired through the dev workflow; `next build` itself is sufficient for
    // the .next bundle which is all we ship).
    run("npm", ["run", "build"], { cwd: ROOT });
  } else {
    warn("skipping build (--no-build)");
    if (!fs.existsSync(path.join(ROOT, ".next", "BUILD_ID")))
      fail(".next/BUILD_ID missing — run without --no-build first.");
  }

  // --- Step 2: assemble dist/app ---
  log("Step 2/5: assemble bundle at dist/app");

  // Preserve the expensive runtime/ (node.exe + python CPython install) across
  // the wipe. We prefer an already-persisted cache (dist/.runtime-cache) so a
  // re-run after a failed/partial build still has the 1GB+ CPython install;
  // otherwise we lift it from the current dist/app/runtime before wiping.
  const runtimeCache = path.join(DIST, ".runtime-cache");
  const appRuntime = path.join(APP, "runtime");
  const runtimeMarker = path.join(runtimeCache, RUNTIME_MARKER);
  const pythonPathIn = (runtimeDir) => {
    const candidates = [
      path.join(runtimeDir, "python", "python.exe"),
      path.join(runtimeDir, "python", "bin", "python.exe"),
    ];
    return candidates.find((candidate) => fs.existsSync(candidate)) || candidates[0];
  };
  const validateCache = () => validateOrInvalidateRuntimeCache({
    cacheDir: runtimeCache,
    nodePath: path.join(runtimeCache, "node.exe"),
    pythonPath: pythonPathIn(runtimeCache),
    markerPath: runtimeMarker,
    expectedFingerprint: RUNTIME_FINGERPRINT,
  });
  const seedCache = (sourceDir) => {
    rmrf(runtimeCache);
    copyDir(sourceDir, runtimeCache);
    fs.writeFileSync(runtimeMarker, `${JSON.stringify(RUNTIME_FINGERPRINT, null, 2)}\n`, "utf8");
    return validateCache();
  };

  let haveCache = validateCache();
  if (!haveCache && fs.existsSync(appRuntime)) {
    log("  validating and caching runtime/ before bundle wipe …");
    haveCache = seedCache(appRuntime);
    if (!haveCache) warn("  removed stale app runtime (expected Node 24.18.0 / Python 3.14.6)");
  }
  if (!haveCache) {
    const staged = path.join(DIST, "runtime");
    if (fs.existsSync(staged)) {
      log(`  validating staged runtime/ from ${path.relative(ROOT, staged)} …`);
      haveCache = seedCache(staged);
      if (!haveCache) fail("staged runtime versions do not match config/runtime-versions.json");
    }
  }

  rmrf(APP);
  fs.mkdirSync(APP, { recursive: true });

  // Files at the bundle root.
  for (const f of ["package.json", "prisma.config.ts", ".npmrc", "next.config.ts"]) {
    if (!copyFile(path.join(ROOT, f), path.join(APP, f)))
      warn(`optional root file missing, skipped: ${f}`);
  }
  // Launchers (kept under packaging/ in the repo, copied to bundle root).
  for (const f of ["start.bat", "stop.bat", "first-run.js"]) {
    if (!copyFile(path.join(PACKAGING, f), path.join(APP, f)))
      fail(`launcher missing: packaging/${f}`);
  }

  // --- Next.js standalone output ---
  // Instead of shipping the full .next + a hoisted node_modules (~65000 files),
  // we use Next's standalone tracing which produces server.js + a minimal
  // node_modules containing only the packages the server actually requires
  // (~1600 files). This cuts the install payload by ~97% in file count, which
  // is the primary fix for the 60% install stall.
  const standaloneDir = path.join(ROOT, ".next", "standalone");
  if (!fs.existsSync(path.join(standaloneDir, "server.js")))
    fail("standalone output missing — ensure `output: 'standalone'` in next.config.ts");
  log("  copying standalone server.js + traced node_modules/ …");
  // server.js — the standalone entry point (replaces `next start`).
  copyFile(path.join(standaloneDir, "server.js"), path.join(APP, "server.js"));
  // .next/server — server-side chunks and route handlers (standalone copies
  // only this subset of .next, not dev/cache/trace).
  copyDir(path.join(standaloneDir, ".next"), path.join(APP, ".next"));
  // node_modules — standalone's traced deps. copyDir resolves pnpm symlinks
  // into real files so they survive packaging and work on the target machine.
  // Exclude dev-only packages that Next's tracer mistakenly includes.
  copyDirExcluding(
    path.join(standaloneDir, "node_modules"),
    path.join(APP, "node_modules"),
    ["electron", "playwright", "playwright-core"]
  );

  // --- Flatten pnpm .pnpm/ store into top-level node_modules/ ---
  // standalone tracing + pnpm's symlink layout produces a .pnpm/ virtual store
  // where packages live at .pnpm/<pkg>@<ver>/node_modules/<pkg>/. Node's
  // require() resolves modules by walking up the directory tree looking for
  // node_modules/<name> — it does NOT look inside .pnpm/. So we must copy
  // every package from .pnpm/<pkg>@<ver>/node_modules/<pkg>/ to the top-level
  // node_modules/<pkg>/ for require() to find them.
  //
  // This is the same flattening that `pnpm install --node-linker=hoisted`
  // does, but applied to the standalone output (which already traced only
  // the needed deps — ~38 packages vs ~65000 files in a full install).
  log("  flattening .pnpm/ store to top-level node_modules/ …");
  const appPnpmDir = path.join(APP, "node_modules", ".pnpm");
  if (fs.existsSync(appPnpmDir)) {
    for (const entry of fs.readdirSync(appPnpmDir, { withFileTypes: true })) {
      if (!entry.isDirectory() || entry.name === "node_modules") continue;
      // Skip dev-only packages that standalone mistakenly included.
      if (/^electron@|^playwright/.test(entry.name)) continue;
      // A .pnpm/<pkg>/ entry may contain multiple packages in its
      // node_modules/ (e.g. pg-types@2.2.0/node_modules/ has pg-types,
      // pg-int8, postgres-array, etc. — pnpm hoists peer deps here).
      // Copy ALL of them, not just the first.
      const innerNm = path.join(appPnpmDir, entry.name, "node_modules");
      if (!fs.existsSync(innerNm)) continue;
      for (const sub of fs.readdirSync(innerNm, { withFileTypes: true })) {
        if (!sub.isDirectory() && !sub.isSymbolicLink()) continue;
        const src = path.join(innerNm, sub.name);
        // For scoped packages (@scope/), sub.name is "@scope" and the real
        // package is inside it (e.g. @swc/helpers). Merge into top-level
        // node_modules/@scope/ rather than skipping if @scope already exists.
        if (sub.name.startsWith("@")) {
          const scopeDst = path.join(APP, "node_modules", sub.name);
          fs.mkdirSync(scopeDst, { recursive: true });
          // Copy each package inside the scope dir
          for (const scopedPkg of fs.readdirSync(src, { withFileTypes: true })) {
            const spSrc = path.join(src, scopedPkg.name);
            const spDst = path.join(scopeDst, scopedPkg.name);
            if (fs.existsSync(spDst)) continue;
            copyDir(spSrc, spDst);
          }
        } else {
          const dst = path.join(APP, "node_modules", sub.name);
          if (fs.existsSync(dst)) continue;
          copyDir(src, dst);
        }
      }
    }
  }

  // Delete the .pnpm virtual store now that flattening has hoisted every
  // package to the top level. Node's require() resolves node_modules/<name> by
  // walking up the dir tree — it never looks inside .pnpm/, so the store is
  // pure dead weight (and it leaks dev-only heavy packages like electron@33
  // that standalone traced but we don't need: ~269MB / ~2700 files here).
  // (packaging lessons "future optimization #2".)
  if (fs.existsSync(appPnpmDir)) {
    const freed = dirSize(appPnpmDir);
    const files = countFiles(appPnpmDir);
    rmrf(appPnpmDir);
    log(`  removed .pnpm/ store (saved ${human(freed)}, ${files} files) …`);
  }

  // --- Mirror flattened node_modules into .next/node_modules/ ---
  // Next.js standalone copies a subset of deps under
  // .next/node_modules/<pkg>-<hash>/ for its own chunk resolution. Those
  // hash-suffixed copies (e.g. @prisma/client-254bee..., better-sqlite3-a9b104...)
  // use dynamic require() for their own transitive deps, and Node's resolver
  // walks UP from the requiring file — so a require('@prisma/client-runtime-utils')
  // inside .next/node_modules/@prisma/client-*/runtime/client.js looks in
  // .next/node_modules/@prisma/client-runtime-utils first, then top-level
  // node_modules/. The top-level flatten above makes the latter work, but
  // mirroring into .next/node_modules/ is belt-and-suspenders: it guarantees
  // resolution regardless of which hash directory issues the require.
  // This is the fix for the v1.0.5 runtime crashes:
  //   "Cannot find module '@prisma/client-runtime-utils'"
  //   "Cannot find module 'bindings'"
  //   "Cannot find module 'file-uri-to-path'"
  const appNextNm = path.join(APP, ".next", "node_modules");
  if (fs.existsSync(appNextNm)) {
    log("  mirroring flattened node_modules into .next/node_modules/ …");
    let mirrored = 0;
    const mirrorEntries = fs.readdirSync(path.join(APP, "node_modules"), { withFileTypes: true });
    for (const entry of mirrorEntries) {
      if (!entry.isDirectory() && !entry.isSymbolicLink()) continue;
      if (entry.name === ".pnpm" || entry.name === ".bin") continue;
      const src = path.join(APP, "node_modules", entry.name);
      if (entry.name.startsWith("@")) {
        // Scope dir: mirror each scoped package
        const scopeDst = path.join(appNextNm, entry.name);
        fs.mkdirSync(scopeDst, { recursive: true });
        for (const scopedPkg of fs.readdirSync(src, { withFileTypes: true })) {
          if (!scopedPkg.isDirectory() && !scopedPkg.isSymbolicLink()) continue;
          const spDst = path.join(scopeDst, scopedPkg.name);
          if (fs.existsSync(spDst)) continue; // don't overwrite standalone's own copies
          copyDir(path.join(src, scopedPkg.name), spDst);
          mirrored++;
        }
      } else {
        const dst = path.join(appNextNm, entry.name);
        if (fs.existsSync(dst)) continue; // don't overwrite standalone's own copies
        copyDir(src, dst);
        mirrored++;
      }
    }
    log(`  mirrored ${mirrored} package(s) into .next/node_modules/`);
  }

  // .next/static — standalone does NOT include static assets; copy separately.
  log("  copying .next/static …");
  copyDir(path.join(ROOT, ".next", "static"), path.join(APP, ".next", "static"));

  // prisma schema + migrations + workers (python scripts).
  // ALSO copy the prisma CLI package (node_modules/prisma). first-run.ts uses
  // better-sqlite3 to apply migrations (so prisma CLI is not used at runtime),
  // BUT build-electron.mjs asserts dist/app/node_modules/prisma exists as a
  // packaging readiness check. Without this copy, `pnpm run electron:build`
  // fails with "dist/app is not ready (missing dist\app\node_modules\prisma)".
  log("  copying prisma/ …");
  copyDir(path.join(ROOT, "prisma"), path.join(APP, "prisma"));
  const rootPrismaCli = path.join(ROOT, "node_modules", "prisma");
  if (fs.existsSync(rootPrismaCli)) {
    log("  copying node_modules/prisma (CLI, required by build-electron.mjs check) …");
    copyDir(rootPrismaCli, path.join(APP, "node_modules", "prisma"));
  } else {
    warn("  node_modules/prisma missing in source tree — build-electron.mjs will fail");
  }
  log("  copying workers/ …");
  copyDir(path.join(ROOT, "workers"), path.join(APP, "workers"));

  // public/ — static assets served at the root by Next.js (e.g. the brand logo
  // referenced via next/image as /logo.png). If absent in the bundle, those
  // assets 404 at runtime and next/image throws "received null".
  if (fs.existsSync(path.join(ROOT, "public"))) {
    log("  copying public/ …");
    copyDir(path.join(ROOT, "public"), path.join(APP, "public"));
  }

  // runtime: restore the cached node.exe + python install, or fall back to a
  // dist/runtime staging dir the operator may have prepared. We never
  // reconstruct the CPython install here — that's a one-time prep step.
  fs.mkdirSync(path.join(APP, "runtime"), { recursive: true });
  if (haveCache) {
    log("  restoring validated cached runtime/ …");
    copyDir(runtimeCache, path.join(APP, "runtime"));
  } else {
    warn(
      "runtime/ missing — start.bat expects runtime\\node.exe and runtime\\python.\n" +
        "  Prepare dist/runtime/{node.exe,python/} matching config/runtime-versions.json."
    );
  }

  const bundledNode = path.join(APP, "runtime", "node.exe");
  if (!fs.existsSync(bundledNode)) {
    fail(`bundled Node is required for native module rebuild: ${bundledNode}`);
  }
  log("Step 2a/5: rebuild native modules for bundled Node ABI");
  try {
    rebuildNativeModulesForBundledNode({
      rootDir: ROOT,
      appDir: APP,
      bundledNodePath: bundledNode,
      platform: "win32",
      arch: "x64",
      log,
      warn,
    });
    smokeNativeModule({ bundledNodePath: bundledNode, appDir: APP });
    log("better-sqlite3 SQL smoke passed (SELECT 42)");
  } catch (error) {
    fail(error.message);
  }

  const beforeTrim = dirSize(APP);
  log(`bundle assembled (pre-trim): ${human(beforeTrim)}`);

  // --- Step 2b: generate legal artifacts (notices + LICENSE) ---
  // Runs BEFORE trimming so stripDocs cannot delete LICENSE/NOTICE files that
  // the scanner needs to read from next/react/react-dom/effect. The output is
  // written into dist/app so electron-builder's extraResources (dist/app →
  // resources/app, filter **/*) carries it into the installer verbatim.
  log("Step 2b/5: generate legal artifacts (notices + LICENSE)");
  try {
    const { generateNotices, writeNotices } = await import("./generate-third-party-notices.mjs");
    const notices = generateNotices({
      nmRoot: path.join(APP, "node_modules"),
      bundleRoot: APP,
      assetManifest: path.join(ROOT, "legal", "assets-notices.json"),
      strict: process.env.NOTICES_STRICT === "1",
    });
    // Write into the bundle's public/legal/ (served by Next.js) and root.
    fs.mkdirSync(path.join(APP, "public", "legal"), { recursive: true });
    writeNotices(notices, path.join(APP, "public", "legal"), pkg.license || "UNLICENSED");
    // Also drop a top-level THIRD-PARTY-NOTICES.txt + LICENSE for easy access.
    copyFile(
      path.join(APP, "public", "legal", "THIRD-PARTY-NOTICES.txt"),
      path.join(APP, "THIRD-PARTY-NOTICES.txt"),
    );
    copyFile(path.join(ROOT, "LICENSE"), path.join(APP, "LICENSE"));
    log(`legal artifacts: ${notices.length} notices + LICENSE written to bundle`);
  } catch (e) {
    warn(`legal artifact generation failed (non-fatal): ${e.message}`);
  }

  // --- Step 3: trim ---
  if (!SKIP_TRIM) {
    log("Step 3/5: trim Python runtime + Node cross-platform binaries");
    trimPython(path.join(APP, "runtime", "python"));
    trimNodeModules(path.join(APP, "node_modules"));
    const afterTrim = dirSize(APP);
    log(`bundle trimmed: ${human(beforeTrim)} -> ${human(afterTrim)} (saved ${human(beforeTrim - afterTrim)})`);
  } else {
    warn("skipping trim (--no-trim)");
  }

  // --- Step 4: compile installer with iscc ---
  // Skipped under --assemble-only: the Electron pipeline (build-electron.mjs)
  // wraps dist/app into an Electron shell and runs its own Synthetix-Electron.iss
  // to produce the final installer. Both .iss files share the same
  // OutputBaseFilename, so running the legacy iscc here risks a stale/colliding
  // .exe shadowing the Electron one.
  if (ASSEMBLE_ONLY) {
    warn("skipping iscc (--assemble-only) — dist/app is ready; run build-electron.mjs to wrap it.");
    const secs = ((Date.now() - startedAt) / 1000).toFixed(1);
    log(`bundle assembled at ${path.relative(ROOT, APP)} (${human(dirSize(APP))}) in ${secs}s`);
    log("done.");
    return;
  }
  log("Step 4/5: compile installer (iscc)");
  fs.mkdirSync(INSTALLER_DIR, { recursive: true });
  const issPath = prepareIss(VERSION);
  run(iscc, [issPath], { cwd: PACKAGING });

  // --- Step 5: report ---
  log("Step 5/5: report");
  const outName = `Synthetix-Setup-v${VERSION}.exe`;
  const outPath = path.join(INSTALLER_DIR, outName);
  if (!fs.existsSync(outPath)) fail(`expected installer not found: ${outPath}`);
  const size = fs.statSync(outPath).size;
  const secs = ((Date.now() - startedAt) / 1000).toFixed(1);
  console.log();
  console.log("  ┌──────────────────────────────────────────────────────┐");
  console.log(`  │  Installer: ${outName.padEnd(26)}         │`);
  console.log(`  │  Size:      ${human(size).padEnd(36)}     │`);
  console.log(`  │  Path:      ${path.relative(ROOT, outPath).padEnd(36)} │`);
  console.log(`  │  Version:   ${(VERSION).padEnd(36)}      │`);
  console.log(`  │  Time:      ${(secs + "s").padEnd(36)}       │`);
  console.log("  └──────────────────────────────────────────────────────┘");
  log("done.");
}

// ---- Python trimming ----// ---- Python trimming ----
// Remove (a) packages the workers never import (dev-environment leakage) and
// (b) non-runtime CPython directories (docs, headers, tcl, static libs, caches).
// Each removal is logged with freed bytes; we stop (not crash) on missing dirs.
// Packages stripped from the bundle during trim. Imported from the shared
// list (python-excluded-packages.mjs) so the notices generator and the trim
// step agree on exactly what does NOT ship.
const PYTHON_UNUSED_PACKAGES = PYTHON_EXCLUDED_PACKAGES;

const PYTHON_UNUSED_DIRS = ["Doc", "include", "libs", "tcl", "share"];

function trimPython(pyRoot) {
  if (!fs.existsSync(pyRoot)) {
    warn(`python runtime missing at ${path.relative(ROOT, pyRoot)} — skipping trim`);
    return;
  }
  const before = dirSize(pyRoot);
  log(`  python pre-trim: ${human(before)}`);

  // Top-level non-runtime CPython directories.
  for (const d of PYTHON_UNUSED_DIRS) {
    const p = path.join(pyRoot, d);
    if (fs.existsSync(p)) {
      const sz = dirSize(p);
      rmrf(p);
      log(`    - ${d}/  (${human(sz)})`);
    }
  }
  // Stray wheels / cached installers inside Lib.
  // Stray wheels / cached installers.
  purgeGlob(path.join(pyRoot, "Lib", "site-packages"), "*.whl");
  purgeGlob(pyRoot, "*.whl");

  // site-packages: unused third-party packages.
  const sp = path.join(pyRoot, "Lib", "site-packages");
  if (fs.existsSync(sp)) {
    for (const pkg of PYTHON_UNUSED_PACKAGES) {
      // packages are often split across <pkg>/ and <pkg>-<ver>.dist-info/
      const variants = [pkg, pkg.replace(/[-_.]+/g, "_")]; // name + normalized form
      const lowerVariants = variants.map((v) => v.toLowerCase());
      const matched = fs.readdirSync(sp).filter((entry) => {
        const le = entry.toLowerCase();
        // Match the bare name, or "<name>-<ver>", or "<name>.<something>".
        return lowerVariants.some(
          (lv) => le === lv || le.startsWith(lv + "-") || le.startsWith(lv + ".")
        );
      });
      for (const m of matched) {
        const p = path.join(sp, m);
        const sz = dirSize(p);
        rmrf(p);
        log(`    - site-packages/${m}  (${human(sz)})`);
      }
    }
  }

  // ---- site-packages deep trim: runtime-irrelevant files inside kept packages ----
  // torch/include (9412 C++ headers, 63MB) — only needed to compile torch
  // extensions; docling just does `import torch`, never compiles anything.
  // torch/lib/*.lib (import libraries, ~45MB) — link-time artifacts; at runtime
  // only the .dll files are loaded. torch/bin/protoc.exe — build tool.
  //
  // NOTE: torchgen is intentionally NOT trimmed. torch 2.13's
  // `torch/utils/_python_dispatch.py` does `import torchgen` at import time
  // (when `import torch` runs), so removing it crashes the Python daemon on
  // startup with `ModuleNotFoundError: No module named 'torchgen'`. The
  // earlier comment ("code-gen, not needed at runtime") was true for older
  // torch versions but is wrong for 2.13+. Keep torchgen in the bundle.
  const deepTrim = [
    [path.join(sp, "torch", "include"), "torch/include (C++ headers)"],
    [path.join(sp, "torch", "bin"), "torch/bin (build tools)"],
  ];
  for (const [p, label] of deepTrim) {
    if (fs.existsSync(p)) {
      const sz = dirSize(p);
      const cnt = countFiles(p);
      rmrf(p);
      log(`    - ${label}  (${human(sz)}, ${cnt} files)`);
    }
  }
  // *.lib files inside torch/lib — import libraries, not needed at runtime.
  purgeGlob(path.join(sp, "torch", "lib"), "*.lib");

  // Python standard-library test suite (Lib/test, 446 files, 32MB).
  const libTest = path.join(pyRoot, "Lib", "test");
  if (fs.existsSync(libTest)) {
    const sz = dirSize(libTest);
    const cnt = countFiles(libTest);
    rmrf(libTest);
    log(`    - Lib/test  (${human(sz)}, ${cnt} files)`);
  }

  // __pycache__ everywhere.
  let cacheBytes = 0;
  let cacheCount = 0;
  const stack = [pyRoot];
  while (stack.length) {
    const cur = stack.pop();
    let entries;
    try {
      entries = fs.readdirSync(cur, { withFileTypes: true });
    } catch {
      continue;
    }
    for (const e of entries) {
      const full = path.join(cur, e.name);
      if (e.name === "__pycache__") {
        cacheBytes += dirSize(full);
        cacheCount++;
        rmrf(full);
      } else if (e.isDirectory()) {
        stack.push(full);
      }
    }
  }
  if (cacheCount) log(`    - __pycache__ (${cacheCount} dirs, ${human(cacheBytes)})`);

  const after = dirSize(pyRoot);
  log(`  python post-trim: ${human(after)} (saved ${human(before - after)})`);
}

function purgeGlob(dir, pattern) {
  if (!fs.existsSync(dir)) return;
  const re = globToRegex(pattern);
  for (const e of fs.readdirSync(dir)) {
    if (re.test(e)) {
      const p = path.join(dir, e);
      const sz = dirSize(p);
      rmrf(p);
      log(`    - ${path.relative(ROOT, p)}  (${human(sz)})`);
    }
  }
}
function globToRegex(g) {
  // Minimal: supports * and . only (sufficient for *.whl).
  let r = "";
  for (const ch of g) r += ch === "*" ? ".*" : ch === "." ? "\\." : ch;
  return new RegExp("^" + r + "$");
}

// ---- Node trimming ----
// Drop platform-specific native subpackages for non-win32-x64 targets, and
// obvious build intermediates. KEEP all *.node and the win32 binaries.
const NODE_PLATFORM_KEEP = "win32-x64";

function trimNodeModules(nmRoot) {
  if (!fs.existsSync(nmRoot)) {
    warn(`node_modules missing at ${path.relative(ROOT, nmRoot)} — skipping trim`);
    return;
  }
  const before = dirSize(nmRoot);
  log(`  node_modules pre-trim: ${human(before)}`);
  let freed = 0;

  // Scoped packages live under @scope/<name>; platform variants look like
  // @next/swc-darwin-x64, @node-rs/jieba-linux-x64-gnu, @img/sharp-libvips-*, etc.
  const isPlatformVariant = (name) => {
    const m = name.match(/-(darwin|linux|android|freebsd|sunos|aix)-(x64|arm64|arm|ia32)(?:-(musl|gnu))?$/i);
    if (!m) return false;
    // Keep the windows + matching-arch variants.
    return !(m[1].toLowerCase() === "win32" || (name.toLowerCase().includes("win32") && name.toLowerCase().includes(NODE_PLATFORM_KEEP)));
  };

  const scanDir = (dir) => {
    for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
      if (!e.isDirectory() && !e.isSymbolicLink()) continue;
      const full = path.join(dir, e.name);
      // Recurse into scopes.
      if (e.name.startsWith("@") && e.isDirectory()) {
        scanDir(full);
        continue;
      }
      if (isPlatformVariant(e.name)) {
        const sz = dirSize(full);
        rmrf(full);
        freed += sz;
        log(`    - ${path.relative(ROOT, full)}  (${human(sz)})`);
      }
    }
  };
  scanDir(nmRoot);

  // Strip *.md / LICENSE / *.map at the top of well-known heavy pure-JS deps.
  // Conservative: only remove within next/, react/, react-dom/, effect/.
  for (const heavy of ["next", "react", "react-dom", "effect"]) {
    const hp = path.join(nmRoot, heavy);
    if (!fs.existsSync(hp)) continue;
    freed += stripDocs(hp);
  }

  const after = dirSize(nmRoot);
  log(`  node_modules post-trim: ${human(after)} (saved ${human(before - after)})`);
}

function stripDocs(pkgDir) {
  let freed = 0;
  const stack = [pkgDir];
  // NEVER strip license/legal files — needed for open-source compliance even
  // though the aggregated THIRD-PARTY-NOTICES.txt covers the bulk; keeping the
  // originals is the conservative choice. `.ts` is also kept now (was removed
  // before) because some loaders probe .d.ts at runtime.
  const keepRe = /^(license|licence|notice|patents|authors|contributors)/i;
  const trimNameRe = /^(changelog|readme|history)/i;
  const docsRe = /\.(md|markdown|map|flow|coffee)$/i;
  while (stack.length) {
    const cur = stack.pop();
    let entries;
    try {
      entries = fs.readdirSync(cur, { withFileTypes: true });
    } catch {
      continue;
    }
    for (const e of entries) {
      const full = path.join(cur, e.name);
      if (e.isDirectory()) {
        stack.push(full);
        continue;
      }
      if (keepRe.test(e.name)) continue; // compliance: always retain
      const isDocName = trimNameRe.test(e.name);
      const isDocExt = docsRe.test(e.name);
      if (isDocName || isDocExt) {
        try {
          freed += fs.statSync(full).size;
          fs.rmSync(full, { force: true });
        } catch {
          /* ignore */
        }
      }
    }
  }
  return freed;
}

// Install a flat (hoisted, no-symlink) production node_modules into the bundle
// dir, using the project's pnpm-lock.yaml for reproducibility. We use pnpm with
// node-linker=hoisted so the result is a real directory tree (not symlinks),
// which packs cleanly and resolves identically on the target machine.
function installNodeModules(bundleDir) {
  // Stage the lockfile + workspace config so pnpm can resolve exactly.
  copyFile(path.join(ROOT, "pnpm-lock.yaml"), path.join(bundleDir, "pnpm-lock.yaml"));
  if (fs.existsSync(path.join(ROOT, "pnpm-workspace.yaml")))
    copyFile(path.join(ROOT, "pnpm-workspace.yaml"), path.join(bundleDir, "pnpm-workspace.yaml"));
  // Force hoisted layout + skip build scripts (native binaries are already
  // built in the source tree and will be copied separately; rebuilding in the
  // bundle risks toolchain mismatches on the build machine).
  fs.writeFileSync(
    path.join(bundleDir, ".npmrc"),
    "node-linker=hoisted\n",
    "utf8"
  );
  const res = spawnSync(
    "pnpm",
    ["install", "--prod", "--config.node-linker=hoisted", "--ignore-scripts", "--no-frozen-lockfile"],
    { cwd: bundleDir, stdio: "inherit", shell: true }
  );
  if (res.status !== 0)
    fail(`pnpm install failed (exit ${res.status}) — see output above.`);
  // pnpm leaves its own metadata dirs behind; not needed at runtime.
  for (const junk of [".pnpm", ".modules.yaml", "pnpm-lock.yaml", "pnpm-workspace.yaml"]) {
    rmrf(path.join(bundleDir, "node_modules", junk));
    rmrf(path.join(bundleDir, junk));
  }
}

// Copy prebuilt native binaries that `pnpm install --ignore-scripts` skips.
// These are downloaded/built by install scripts, so a --prod --ignore-scripts
// install leaves the JS wrappers present but the .node/.exe absent. The build
// machine already has them in the source node_modules (under .pnpm/<pkg>/...);
// we resolve the real path and copy just the binary into the hoisted bundle.
function copyNativeBinaries(bundleNm) {
  const srcNm = path.join(ROOT, "node_modules");
  // [source glob under .pnpm, destination under bundle node_modules]
  const targets = [
    ["better-sqlite3/build/Release/better_sqlite3.node", "better-sqlite3/build/Release/better_sqlite3.node"],
    ["@prisma/engines/schema-engine-windows.exe", "@prisma/engines/schema-engine-windows.exe"],
  ];
  for (const [rel, dstRel] of targets) {
    const src = resolvePnpmReal(srcNm, rel);
    const dst = path.join(bundleNm, dstRel);
    if (!src) {
      warn(`native binary not found in source, skipping: ${rel}`);
      continue;
    }
    fs.mkdirSync(path.dirname(dst), { recursive: true });
    fs.copyFileSync(src, dst);
    log(`    + ${dstRel}  (${human(fs.statSync(src).size)})`);
  }
}

// Find the real file behind pnpm's symlink layout. The real contents live at
// .pnpm/<pkg>@<ver>/node_modules/<rel>; we walk .pnpm and, at every directory
// literally named "node_modules", test whether <that dir>/<rel> exists.
function resolvePnpmReal(srcNm, rel) {
  const direct = path.join(srcNm, rel);
  if (fs.existsSync(direct)) return fs.realpathSync(direct);
  const pnpm = path.join(srcNm, ".pnpm");
  if (!fs.existsSync(pnpm)) return null;
  let hit = null;
  const stack = [pnpm];
  while (stack.length && !hit) {
    const cur = stack.pop();
    let entries;
    try {
      entries = fs.readdirSync(cur, { withFileTypes: true });
    } catch {
      continue;
    }
    for (const e of entries) {
      const full = path.join(cur, e.name);
      if (e.name === "node_modules" && e.isDirectory()) {
        const candidate = path.join(full, rel);
        if (fs.existsSync(candidate)) {
          hit = candidate;
          break;
        }
        // Don't descend into node_modules generally (huge); we only probe rel.
      } else if (e.isDirectory()) {
        stack.push(full);
      }
    }
  }
  return hit ? fs.realpathSync(hit) : null;
}

// ---- iscc .iss preparation ----
// The committed packaging/Synthetix.iss uses placeholders that we substitute
// with the real version, so the version never drifts from package.json.
function prepareIss(version) {
  const templatePath = path.join(PACKAGING, "Synthetix.iss");
  const outPath = path.join(PACKAGING, "Synthetix.generated.iss");
  let src = fs.readFileSync(templatePath, "utf8");
  // Replace version tokens regardless of the committed value.
  src = src.replace(/#define MyAppVersion .*/, `#define MyAppVersion "${version}"`);
  src = src.replace(/OutputBaseFilename=Synthetix-Setup-v[0-9.]+/, `OutputBaseFilename=Synthetix-Setup-v${version}`);
  fs.writeFileSync(outPath, src, "utf8");
  log(`  prepared ${path.relative(ROOT, outPath)} (v${version})`);
  return outPath;
}

