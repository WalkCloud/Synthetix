#!/usr/bin/env node
/**
 * Synthetix — application metadata generator.
 *
 * Produces three artifacts from a single source of truth (package.json + git):
 *
 *   1. src/generated/app-version.ts — STATIC (git-tracked): name, version,
 *      license. These rarely change and are safe to commit, so a clean
 *      checkout shows the real version without running this script.
 *
 *   2. public/build-info.json       — DYNAMIC (git-ignored): buildTime and
 *      git commit short hash. These change on every build; committing them
 *      would leave the working tree perpetually dirty. The JSON is served via
 *      Next.js static hosting and fetched at runtime; if absent (clean
 *      checkout), the About dialog falls back to { buildTime: null, commit:
 *      "dev" }. Using public/*.json (not a TS import) avoids webpack failing
 *      to statically resolve a git-ignored module on a clean checkout.
 *
 *   3. public/legal/LICENSE.txt     — copy of root LICENSE for the About
 *      dialog's "View License" link.
 *
 * Strategy A from docs/about-dialog-design-and-compliance-plan-2026-07-08.md
 * §7.1.1 — split static identity from dynamic build provenance.
 *
 * Usage:  node scripts/generate-app-metadata.mjs
 */
import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), "..");
const GEN_DIR = path.join(ROOT, "src", "generated");
const PUBLIC_DIR = path.join(ROOT, "public");

// ---------- helpers ----------
function log(...m) {
  console.log(`\x1b[36m[gen-meta]\x1b[0m`, ...m);
}

function readJson(p) {
  return JSON.parse(fs.readFileSync(p, "utf8"));
}

/** Return the git short commit hash, or null if git is unavailable / not a repo. */
function readCommit() {
  const res = spawnSync("git", ["rev-parse", "--short", "HEAD"], {
    cwd: ROOT,
    encoding: "utf8",
    shell: process.platform === "win32",
  });
  if (res.status !== 0 || !res.stdout) return null;
  return res.stdout.trim() || null;
}

function isoUtc() {
  return new Date().toISOString();
}

// ---------- main ----------
function main() {
  const pkg = readJson(path.join(ROOT, "package.json"));
  fs.mkdirSync(GEN_DIR, { recursive: true });

  // 1) STATIC app-version.ts (git-tracked).
  const versionTs = `// AUTO-GENERATED by scripts/generate-app-metadata.mjs — do not edit by hand.
// Static identity fields sourced from package.json. Safe to commit.
export const appVersion = ${JSON.stringify(
    {
      name: pkg.name,
      version: pkg.version,
      license: pkg.license || "UNLICENSED",
    },
    null,
    2,
  )} as const;
`;
  fs.writeFileSync(path.join(GEN_DIR, "app-version.ts"), versionTs, "utf8");
  log(`wrote src/generated/app-version.ts (v${pkg.version})`);

  // 2) DYNAMIC build-info.json (git-ignored) in public/ for runtime fetch.
  const commit = readCommit();
  const buildInfo = {
    buildTime: isoUtc(),
    commit: commit ?? "dev",
  };
  fs.writeFileSync(
    path.join(PUBLIC_DIR, "build-info.json"),
    JSON.stringify(buildInfo, null, 2),
    "utf8",
  );
  log(`wrote public/build-info.json (commit ${buildInfo.commit})`);

  // 3) Copy root LICENSE → public/legal/LICENSE.txt for Web/Electron serving,
  //    and copy the curated core-components manifest alongside it.
  const licenseSrc = path.join(ROOT, "LICENSE");
  fs.mkdirSync(path.join(PUBLIC_DIR, "legal"), { recursive: true });
  if (fs.existsSync(licenseSrc)) {
    fs.copyFileSync(licenseSrc, path.join(PUBLIC_DIR, "legal", "LICENSE.txt"));
    log("copied LICENSE → public/legal/LICENSE.txt");
  }
  const coreManifest = path.join(ROOT, "legal", "core-components.json");
  if (fs.existsSync(coreManifest)) {
    fs.copyFileSync(coreManifest, path.join(PUBLIC_DIR, "legal", "core-components.json"));
    log("copied core-components.json → public/legal/core-components.json");
  }
}

main();
